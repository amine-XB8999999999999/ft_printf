#include "ft_printf.h"

int	ft_putnbr_unsa(unsigned int n)
{
	char	c;
	int		i;

	i = 0;
	if (n < 0)
	{
		i += write(1, "-", 1);
		n = -n;
	}
	if ((n / 10) > 0)
		i += ft_putnbr((n / 10));
	c = (n % 10) + '0';
	i += write(1, &c, 1);
	return (i);
}
