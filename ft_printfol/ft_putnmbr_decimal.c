#include "ft_printf.h"

int	ft_putnbr(int n)
{
	char	c;
	int	nmb;

	if (n == INT_MIN)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write(1, "-", 1);
		n = -n;
	}
	while ((n / 10) > 0)
	{
		
		c = (n % 10) + '0';
		write(1, &c, 1);
		
	}
		
	c = (n % 10) + '0';
	write(1, &c, 1);
}